func TestCubicCompute1D2P(t *testing.T) {
	x := []float64{0, 1}
	y := []float64{math.E, math.Pi}
	xnew := []float64{0.42, 0.69}
	ynew := []float64{2.8960723750139596e+00, 3.0103662977992611e+00}

	cubic := NewCubic(x, y)
	assert.EqualWithin(cubic.Evaluate(xnew), ynew, 1e-15, t)
}

